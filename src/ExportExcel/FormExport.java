    private void btnChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseActionPerformed
        // TODO add your handling code here:
        int Database =Integer.parseInt(txtChoose.getText());
        
        switch(Database){
            case 1:
                reset();
                txt1.setText("Kode Anggota");
                txt2.setText("Nama");
                txt3.setText("Alamat");
                txt4.setText("Jekel");
                controller.viewTable1();
                
                break;
            case 2:
                reset();
                txt1.setText("Kode Buku");
                txt2.setText("Judul Buku");
                txt3.setText("Penerbit");
                txt4.setText("Tahun Terbit");
                controller.viewTable2();
                
                break;
            case 3:
                reset();
                txt1.setText("Kode Anggota");
                txt2.setText("Kode Buku");
                txt3.setText("TglPinjam");
                txt4.setText("TglKembali");
                txt5.setText("Status");
                controller.viewTable3();
                break;
            case 4:
                reset();
                txt1.setText("Kode Anggota");
                txt2.setText("Kode Buku");
                txt3.setText("TglPinjam");
                txt4.setText("TglKembali");
                txt5.setText("Terlambat");
                txt6.setText("Denda");
                controller.viewTable4();
                break;
                
        }
    }//GEN-LAST:event_btnChooseActionPerformed

