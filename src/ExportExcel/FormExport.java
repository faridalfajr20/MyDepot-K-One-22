        switch(Database){
            case 1:
                controller.anggota();
                break;
            case 2:
                reset();
                controller.buku();
                break;
            case 3:
                reset();
                controller.peminjaman();
                break;
            case 4:
                reset();
                controller.pengembalian();
                break;
                
        } 
